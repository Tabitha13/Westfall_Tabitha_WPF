//Average shopping bill

var weeklyAmounts = [120, 116, 142, 109, 125]; //weekly shopping totals
var weeklyTotal = weeklyAmounts[0] + weeklyAmounts[1] + weeklyAmounts[2] + weeklyAmounts[3] + weeklyAmounts[4]; //adds up totals
var weeklyAverage = weeklyTotal / 5; //finds average
console.log("You have spent a total of $" + weeklyTotal + " on groceries over 5 weeks. That is an average of $" + weeklyAverage + " per week."); //displays answer in console